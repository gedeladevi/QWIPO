FormateItems1(items1:DalsAndPulses[]): DalsAndPulses[] {
  let itemcount = items1.length;
  let columns  = 2;
  let rows = Math.round (itemcount/columns);
  let index = -1;
   

for ( let y = 0; y < rows; y++ ) {   
  for ( let x = 0; x < columns; x++ ) {
  index++; 
  
 if(index <= (itemcount-1)){
  items1[index].Row = y;
  items1[index].Col = x;  
 }

      
  }
} 
return items1;
}

